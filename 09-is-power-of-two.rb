# Write a method that takes in a number and returns true if it is a
# power of 2. Otherwise, return false.
#
# You may want to use the `%` modulo operation. `5 % 2` returns the
# remainder when dividing 5 by 2; therefore, `5 % 2 == 1`. In the case
# of `6 % 2`, since 2 evenly divides 6 with no remainder, `6 % 2 == 0`.
#
# Difficulty: medium.

def is_power_of_two?(num)

  # Reject 0 or negative numbers, else take the square root of the number provided
  num <= 0 ? (return false) : base = Math.sqrt(num)

  # Check the sqrt() result to see if it's a whole number, if so we've got a
  # power of two. base.is_a?(Integer) will not work here as sqrt() returns floats
  base % base.to_i == 0 ? (return true) : (return false)

end

